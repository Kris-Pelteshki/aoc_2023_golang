package main

import (
	"testing"
)

var example = `467..114..
...*......
..35..633.
......#...
617*......
.....+.58.
..592.....
......755.
...$.*....
.664.598..`

var test_input_2 = `........
.24..4..
......*.`

var test_input_3 = `1-......
........
......-1
........
.24.....
......*4`

func Test_part1(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "example",
			input: example,
			want:  4361,
		},
		{
			name:  "test_input_2",
			input: test_input_2,
			want:  4,
		},
		{
			name:  "test_input_3",
			input: test_input_3,
			want:  6,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := part1(tt.input); got != tt.want {
				t.Errorf("part1() = %v, want %v", got, tt.want)
			}
		})
	}
}

